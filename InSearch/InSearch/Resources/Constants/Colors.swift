import UIKit

extension UIColor {
    enum Red {
        static let main = UIColor(red: 0.96, green: 0.26, blue: 0.16, alpha: 1)
    }

    enum Gray {
        static let main = UIColor(red: 0.67, green: 0.67, blue: 0.67, alpha: 1)
        static let light = UIColor(red: 0.94, green: 0.94, blue: 0.94, alpha: 1)
    }
}
